# Two-column layout: Top Threat IPs (left) and Recent Alerts (right)

# ── Top IPs ─────────────────────────────────────────────
col3, col4 = st.columns([2, 3])  # 40/60 split

with col3:
    # ─────────────────────────────────────────────────────
    # TOP THREAT IPs TABLE
    # ─────────────────────────────────────────────────────
    # Shows the most active attacker IP addresses with threat bars
    
    st.markdown('<div class="section-title">Top threat IPs</div>', unsafe_allow_html=True)
    
    if len(df) and "ip" in df.columns:
        # Count threats per IP and get top 8
        ip_counts = df.groupby("ip").size().sort_values(ascending=False).head(8)
        max_count = ip_counts.max()

        # Build HTML table rows
        rows_html = ""
        for ip, count in ip_counts.items():
            # Calculate threat bar width (0-100%)
            bar_w = int((count / max_count) * 100)
            rows_html += f"""
            <tr>
              <td>{ip}</td>
              <td>{count}</td>
              <td><div class="threat-bar" style="width:{bar_w}%"></div></td>
            </tr>"""

        # Render custom HTML table
        st.markdown(f"""
        <table class="ip-table">
          <tr><th>IP Address</th><th>Hits</th><th>Threat</th></tr>
          {rows_html}
        </table>""", unsafe_allow_html=True)

with col4:
    # ─────────────────────────────────────────────────────
    # RECENT ALERTS FEED
    # ─────────────────────────────────────────────────────
    # Shows the 15 most recent threats with color-coded badges
    
    st.markdown('<div class="section-title">Recent alerts</div>', unsafe_allow_html=True)
    
    recent = df.head(15)  # Get 15 most recent (already sorted by timestamp desc)
    
    for _, row in recent.iterrows():
        # Extract row data with fallbacks
        level     = str(row.get("level", "WATCH"))
        ip        = str(row.get("ip", "—"))
        attack    = str(row.get("attack_type", "—")).replace("_", " ")
        conf      = float(row.get("confidence", 0))
        ts        = row.get("timestamp")
        ts_str    = ts.strftime("%H:%M:%S") if pd.notna(ts) else "—"

        # Render alert row with custom HTML
        st.markdown(f"""
        <div class="alert-row {level}">
          <span class="badge {level}">{level}</span>
          <span class="ip-text">{ip}</span>
          <span class="attack-text">{attack.title()}</span>
          <span class="conf-text">conf: {conf:.0%}</span>
          <span class="time-text">{ts_str}</span>
        </div>""", unsafe_allow_html=True)


