def load_demo_data() -> pd.DataFrame:
    """
    Generate realistic synthetic alert data for demonstration purposes.
    
    This function creates fake but realistic-looking cyber attack data when no
    real data sources are available. Used as a fallback to ensure the dashboard
    always has something to display.
    
    Returns:
        pd.DataFrame: Synthetic threat data with columns:
            - timestamp: When the threat occurred
            - level: ALERT or WATCH
            - ip: Source IP address
            - attack_type: Type of attack (brute_force, port_scan, etc.)
            - confidence: Detection confidence (0.0-1.0)
            - url: Targeted URL
            - status_code: HTTP status code
            - rules: Triggered detection rules
    
    Data Characteristics:
        - 120 total events over 6 hours
        - 28% attack rate (rest filtered out)
        - Mix of ALERT and WATCH level threats
        - Realistic IP addresses and attack patterns
    """
    random.seed(42)  # Reproducible random data
    np.random.seed(42)

    # Predefined IP pools for realistic data
    attack_ips = ["192.168.0.21", "203.0.113.5", "45.33.32.156", "198.20.69.74"]
    normal_ips = [f"10.0.0.{i}" for i in range(1, 12)]
    attack_types = ["brute_force", "port_scan", "web_attack", "sql_injection", "ddos"]

    rows = []
    base = datetime.now() - timedelta(hours=6)  # Start 6 hours ago

    for i in range(120):
        # Generate timestamp with some randomness
        ts   = base + timedelta(minutes=i * 3 + random.randint(0, 2))
        
        # 28% chance of attack
        is_attack = random.random() < 0.28
        ip    = random.choice(attack_ips) if is_attack else random.choice(normal_ips)
        
        # Determine threat level (ALERT = high confidence, WATCH = medium)
        level = "ALERT" if (is_attack and random.random() > 0.4) else ("WATCH" if is_attack else None)
        
        # Skip non-threats (normal traffic)
        if not level:
            continue
            
        rows.append({
            "timestamp"  : ts,
            "level"      : level,
            "ip"         : ip,
            "attack_type": random.choice(attack_types) if is_attack else "unknown",
            "confidence" : round(random.uniform(0.65, 0.99) if level == "ALERT" else random.uniform(0.3, 0.65), 2),
            "url"        : random.choice(["/login", "/admin", "/wp-login.php", "/api/data", "/phpmyadmin"]),
            "status_code": random.choice([401, 403, 404, 200]),
            "rules"      : random.choice(["brute_force,off_hours", "suspicious_agent", "high_error_rate", ""]),
        })

    return pd.DataFrame(rows)


@st.cache_data(ttl=10)  # Cache for 10 seconds to improve performance
def load_data() -> pd.DataFrame:
    """
    Load threat data with priority-based source selection.
    
    Data Source Priority:
        1. CIC-IDS 2017 Dataset (if available) - Real network attack data
        2. AlertAgent CSV logs (if available) - Pipeline-generated alerts
        3. Demo data (fallback) - Synthetic data for testing
    
    Returns:
        pd.DataFrame: Threat data with standardized columns:
            - timestamp (datetime, tz-naive UTC)
            - level (str): ALERT or WATCH
            - ip (str): Source IP address
            - attack_type (str): Attack classification
            - confidence (float): Detection confidence 0.0-1.0
            - url (str): Targeted URL
            - status_code (int): HTTP status code
            - rules (str): Comma-separated detection rules
    
    Encoding Handling:
        Tries multiple encodings (utf-8, latin-1, cp1252) to handle
        Windows-encoded CSV files that may contain non-UTF-8 characters.
    
    Timezone Handling:
        All timestamps are normalized to tz-naive UTC for consistent
        filtering and comparison operations.
    """
    
    # Priority 1: CIC-IDS dataset (real network attack data)
    if CICIDS_PATH.exists():
        try:
            from dashboard.cicids_loader import load_cicids_for_dashboard
            df = load_cicids_for_dashboard(str(CICIDS_PATH))
            print(f"[Dashboard] Loaded {len(df)} threats from CIC-IDS dataset")
            return df
        except Exception as e:
            st.warning(f"Failed to load CIC-IDS dataset: {e}. Falling back to alerts.csv")
    
    # Priority 2: AlertAgent logs (pipeline output)
    if CSV_PATH.exists():
        # Try multiple encodings to handle Windows CSV files
        for enc in ("utf-8", "latin-1", "cp1252"):
            try:
                df = pd.read_csv(CSV_PATH, encoding=enc)
                break
            except UnicodeDecodeError:
                continue
        
        # Parse timestamps and normalize to tz-naive UTC
        df["timestamp"] = pd.to_datetime(df["timestamp"], errors="coerce", utc=True)
        df["timestamp"] = df["timestamp"].dt.tz_localize(None)  # Strip timezone
        
        print(f"[Dashboard] Loaded {len(df)} alerts from logs/alerts.csv")
        return df
    
    # Priority 3: Demo data (fallback)
    print("[Dashboard] Using demo data (no real data sources found)")
    return load_demo_data()


